package phase1.basics_of_programming.operators;

public class relational_operator {
    public static void main(String args[]) {
        int a = 10 , b = 20;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);         
        System.out.println(a <= b);
        System.out.println(a >= b);
    }
    
}
