package phase1.basics_of_programming.operators;
public class arith_operators {
    public static void main(String args[])
    {
        int a =50;
        int b = 70;
        
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        
    }
}

